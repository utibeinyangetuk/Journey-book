<template>
	<div class="container">
		<div class="greeting">
			<h1>{{ greeting }}, <input type="text" placeholder="your name" v-model="user" /></h1>
		</div>
		<div class="actions">
			<button>create a new note</button>
			<button>view existing notes</button>
		</div>
	</div>
</template>

<script setup>
	import { ref, watch, onmounted } from 'vue'

	// variables
	const user = ref('')
	const greeting = ref('')

	// get hours function block
	const time0fday = new Date().getHours()
	if (time0fday >= 0 && time0fday <= 11) {
		greeting.value = 'Good morning'
	} else if (time0fday >= 12 && time0fday <= 17) {
		greeting.value = 'Good afternoon'
	} else {
		greeting.value = 'Good evening'
	}
</script>

